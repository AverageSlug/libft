#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	char	*l;

	l = 0;
	while (*s)
	{
		if (*s == c)
			l = (char*)s;
		s++;
	}
	if (l)
		return (l);
	if (c == '\0')
		return ((char*)s);
	return (0);
}
